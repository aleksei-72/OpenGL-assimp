std::vector<Vertex> toVector(float *buffer, unsigned int bufferSize, float scale)
{
    std::vector<Vertex> vertices;

    for (unsigned int i = 0; i < bufferSize; i += 8)
    {
        for (unsigned int j = 0; j < 3; j++)
        {
            buffer[i+j] *= scale;
        }

        vertices.push_back(
            {
                {
                    buffer[i],
                    buffer[i+1],
                    buffer[i+2]
                },
                {
                    buffer[i+3],
                    buffer[i+4]
                },
                {
                    buffer[i+5],
                    buffer[i+6],
                    buffer[i+7]
                }
            }
        );
    }

    return vertices;
}
